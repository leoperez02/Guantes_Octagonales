    public void run(){
        SisGRE.start();
        while(!SisGRE.isFinish() && flagInterrupt){
            //System.out.println("Corriendo el hilo");
            jDialog3.setVisible(true);
            jLabel6.setText(SisGRE.getEstado()+" ... ");
            jProgressBar1.setValue(jProgressBar1.getValue()+1);
            jProgressBar1.setString(jProgressBar1.getValue()+"% finalizado");
            try{
                Thread.sleep(125);
            }catch (InterruptedException e){}
        }
        if(flagInterrupt){
            jDialog3.setVisible(false);
            jDialog2.setVisible(true);
        }
        else{
             //SisGRE.detener();
             flagInterrupt=true;
        }
    }
