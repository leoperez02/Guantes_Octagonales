    private void jFileChooser1PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jFileChooser1PropertyChange
        try {
            // TODO add your handling code here:
            if(flag){
                if(jFileChooser1.getSelectedFile()!=null){
                    jTextField1.setText(jFileChooser1.getSelectedFile().getCanonicalPath());
                    recibidos=jTextField1.getText();
                    if(!"".equals(jTextField2.getText()) && !"Archivo de texto plano".equals(jTextField2.getText()))
                        jButton3.setEnabled(true);
                }    
            }
            else{
                if(jFileChooser1.getSelectedFile()!=null){
                    jTextField2.setText(jFileChooser1.getSelectedFile().getCanonicalPath());
                    enviados=jTextField2.getText();
                    if(!"".equals(jTextField1.getText()) && !"Archivo de texto plano".equals(jTextField1.getText()))
                        jButton3.setEnabled(true);
                }
            }    
        }
        catch(IOException e){}
    }//GEN-LAST:event_jFileChooser1PropertyChange

